    msg.style.cssText = `margin-bottom: 12px; padding: 12px; border-radius: 12px; font-size: 0.72rem; line-height: 1.6; background: rgba(255,255,255,0.02); border-left: 4px solid ${borderColor}; transition: 0.3s;`;
    
